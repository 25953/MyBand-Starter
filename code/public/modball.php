<div class="gumball-box2">
<p>De allereerste Modball Rally vond plaats in juni 2007 met meer dan 150 auto's in het beginraster van de O2 Arena. 
	De route begon in Londen en waagde zich met een casinoparty door Milaan, Amsterdam, Keulen, Praag, 
	Wenen met een live optreden van de Fuel Girls voordat het eindigde met een awards-feest in Lyon. 
	<br><br>
	Op dag twee van de rally maakte de Modball de Duitse televisie en de voorpagina van elke Duitse krant omdat hij omringd was door 100 Duitse politie-auto's tijdens een autoshow in de Keulse Arena. 
	Ondanks het feit dat ze toestemming hadden om door Duitsland te rijden, werden drie auto's betrapt op hardrijden op de vluchtstrook in Düsseldorf met meer dan 240 km/u, 
	waardoor de chef van de Duitse politie het evenement kwalificeerde als een illegale straatrace. 
	<br><br>
	Het resultaat liet elk Modball-voertuig met een boete van € 173 en een politie-escorte naar België meer dan 10 uur duren. 
	Dit weerhield de Modballers er echter niet van om het volgende feest in Praag te maken terwijl ze hun auto's de nacht door reden. Om deze reden ging de prijs voor 'Spirit of the Modball' naar elk team op de rally.
</p>
