<p id="gumball-text">De Modball Rally is een internationale autorally met een andere route elk jaar.
	Opgericht in 2007, nemer er ongeveer 100-180 auto's deel per evenement, dat zijn meestal gemodificeerde supercars.
	De deelnemende auto's stoppen echter niet bij sportwagens en de rally heeft geresulteerd in inzendingen zoals brandweerwagens, 
	limousines en alles wat je maar kunt bedenken.
	<br><br>
	De Rally is geen race noch een rally in de traditionele zin zoals de WRC; 
	er is geen prijs voor de snelste of officiële tijd van welke aard dan ook. De organisatoren benadrukken dat het een avontuur is en niet een race.
</p>
