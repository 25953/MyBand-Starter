<p id="gumball-text">In het begin van de 20e eeuw werd Erwin Baker bekend door zijn races in de Verenigde Staten waarbij hij zo snel mogelijk van de Oost- naar de Westkust van de Verenigde Statenkust reed. 
In 1933 reed hij deze route binnen een tijdsbestek van ongeveer 54 uren en kreeg hij hierdoor de bijnaam 'Cannon Ball'.
Naar zijn gedachtenis stichtte Brock Yates, oprichter van 'Car & Driver' magazine, in de jaren zeventig de 'Cannonball Baker Sea-To-Shining-Sea Memorial Trophy Dash',
bij de meesten eerder bekend als de Cannonball Run.
<br><br>
Dit evenement vormde in 1976 de inspiratie voor de films The Cannonball Run en The Gumball Rally.
De Cannonball Run werd ten slotte geannuleerd in 1979 vanwege de grote media-aandacht die het evenement aangetrokken had,
evenals de druk die politieke groeperingen uitoefenden om deze rally een halt toe te roepen.</p>
</div>
