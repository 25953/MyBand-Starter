<div class="gumball-box2">
<p>Maximillion Cooper hield in 1999 de Gumball 3000 Rally boven het doopvont.
Geïnspireerd door de films van de jaren zeventig besloot hij om 50 vrienden uit te nodigen en zo van Londen naar Rimini, 
Italië te rijden.
<br><br>
De deelnemers reden iedere dag van het éne luxueuze hotel naar het andere waar 's nachts gefeest werd. 
Door de vele supercars en vele bekende artiesten die meededen, heeft deze rally sindsdien veel aan populariteit gewonnen. 
Het evenement groeide ieder jaar en in 2002 bereikte de rally de Verenigde Staten. Afrika mocht zijn eerste glimp van de Gumball Rally opvangen in 2004 toen, 
op uitnodiging van koning Mohammed VI van Marokko, hun wegen opengesteld werden voor de 192 deelnemers.
<br><br>
In 2007 had de Gumball zijn eerste dode te betreuren. 
De Gumball-deelnemers van Team 75 raakten in Macedonië betrokken bij een fataal ongeluk. In hun Porsche 911 Turbo Techart kwamen ze frontaal in botsing met een Volkswagen Golf, 
waarin een echtpaar reed. De 67-jarige bestuurder van de Golf en zijn vrouw kwamen daarbij om het leven.</p>
